const model = require("../models/rankingsModel");

// #############################################################################################################################################
// DEFINE CONTROLLER FUNCTION FOR READ ALL RANKS
// #############################################################################################################################################
module.exports.readAllRank = (req, res, next) => {
    const callback = (error, results, fields) => {
        if (error) {
            console.error("Error readAllRank:", error);
            res.status(500).json(error);
        }
        else res.status(200).json(results);
    }

    model.selectAll(callback);
}